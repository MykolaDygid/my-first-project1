# Створення множин
A = {1, 2, 3, 4, 5}
B = {3, 4, 5, 6, 7}
# Пошук спільних елементів
intersection = A & B
# Виведення результату
print(intersection)
# Перетин множин допомагає знаходити спільні елементи
# між двома наборами даних.
# Наприклад, спільних учнів у двох гуртках або однакові товари у списках.