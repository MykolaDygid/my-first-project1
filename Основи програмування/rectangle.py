# Обчислення параметрів прямокутника
print("ПАРАМЕТРИ ПРЯМОКУТНИКА")
print("=" * 35)
# Розміри прямокутника
width = 12   # Ширина (см)
height = 5   # Висота (см)
# Обчислення площі
area = width * height  # Формула площі
# Обчислення периметра
perimeter = 2 * (width + height)  # Формула периметра
# Вивід результатів
print(f"Ширина: {width} см")        # Вивід ширини
print(f"Висота: {height} см")       # Вивід висоти
print(f"Площа: {area} см²")         # Вивід площі
print(f"Периметр: {perimeter} см")  # Вивід периметра
print("=" * 35)