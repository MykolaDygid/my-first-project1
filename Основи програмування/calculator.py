# calculator.py
# Базовий калькулятор та робота з операторами і функціями
print("КАЛЬКУЛЯТОР")
print("=" * 30)
# Оголошення змінних
a = 20  # Перше число
b = 6   # Друге число
# Арифметичні операції
print(f"{a} + {b} = {a + b}")    # Додавання
print(f"{a} - {b} = {a - b}")    # Віднімання
print(f"{a} * {b} = {a * b}")    # Множення
print(f"{a} / {b} = {a / b}")    # Ділення
print(f"{a} // {b} = {a // b}")  # Цілочисельне ділення
print(f"{a} % {b} = {a % b}")    # Остача від ділення
print(f"{a} ** {b} = {a ** b}")  # Піднесення до степеня
print("=" * 30)
# Робота зі складеними операторами
score = 0  # Лічильник
score += 10  # Додаємо 10
print("Score після += 10:", score)
score += 5   # Додаємо 5
print("Score після += 5:", score)
score -= 3   # Віднімаємо 3
print("Score після -= 3:", score)
score *= 2   # Множимо на 2
print("Score після *= 2:", score)
print("=" * 30)
# Вбудовані функції
total = 99.99 * 1.20  # Розрахунок суми з ПДВ
total_rounded = round(total, 2)  # Округлення до 2 знаків
print("Сума з ПДВ:", total_rounded)
# Оцінки
grade1 = 85  # Перша оцінка
grade2 = 92  # Друга оцінка
grade3 = 78  # Третя оцінка
print("Найвища оцінка:", max(grade1, grade2, grade3))  # Максимум
print("Найнижча оцінка:", min(grade1, grade2, grade3)) # Мінімум