# greeting.py
# Програма для привітання користувача та роботи з f-рядками
# Отримуємо дані від користувача
name = input("Введіть ваше ім'я: ")
age = int(input("Введіть ваш вік: "))  # Конвертуємо вік у число
# Розрахунок віку на наступний рік
next_year_age = age + 1
# Виведення з декоративною рамкою
print("=" * 30)
print(f"Привіт, {name}!")
print(f"Зараз вам {age} років.")
print(f"Наступного року вам буде {next_year_age} років.")
print("=" * 30)