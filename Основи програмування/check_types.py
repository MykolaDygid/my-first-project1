# Оголошення змінної temp1 типу int
temp1 = 42
# Оголошення змінної temp2 типу float
temp2 = 3.14
# Оголошення змінної temp3 типу str
temp3 = "Python"
# Оголошення змінної temp4 типу bool
temp4 = True
# Оголошення змінної temp5 типу int (0 — це int)
temp5 = 0
# Виведення значення temp1 та його типу
print("temp1 =", temp1, "| тип:", type(temp1))
# Виведення значення temp2 та його типу
print("temp2 =", temp2, "| тип:", type(temp2))
# Виведення значення temp3 та його типу
print("temp3 =", temp3, "| тип:", type(temp3))
# Виведення значення temp4 та його типу
print("temp4 =", temp4, "| тип:", type(temp4))
# Виведення значення temp5 та його типу
print("temp5 =", temp5, "| тип:", type(temp5))
# Перетворення логічного значення temp4 у числове (bool → int)
temp4_as_int = int(temp4)
# Виведення результату перетворення
print("temp4 як число:", temp4_as_int)