numbers = list(range(10))  # від 0 до 9

print("Перші 4 елементи:", numbers[:4])
print("Останні 3 елементи:", numbers[-3:])
print("Кожен другий елемент:", numbers[::2])

reversed_list = numbers[::-1]
print("Реверс списку:", reversed_list)

# Крок у зрізі (третій параметр) визначає, як рухатися по списку.
# Якщо крок = -1, список проходиться у зворотному напрямку (реверс).