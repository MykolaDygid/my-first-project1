# Завдання 1
fruits = ["яблуко", "банан", "апельсин", "груша", "ківі"]

print("Перший елемент:", fruits[0])
print("Третій елемент:", fruits[2])
print("Останній елемент:", fruits[-1])

# Нумерація починається з 0, тому що Python (як і більшість мов програмування)
# використовує індексацію з нуля: перший елемент має індекс 0, другий — 1 і т.д.


# Завдання 2
numbers = list(range(1, 16))
print("Список чисел:", numbers)

# range(1, 16) створює спеціальний об'єкт-діапазон, а не список.
# Якщо його просто вивести, ми побачимо не самі числа, а опис об'єкта.
# Тому потрібно перетворити його в список за допомогою list().