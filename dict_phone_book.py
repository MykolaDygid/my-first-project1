# Телефонна книга
phone_book = {
    "Іван": "0671234567",
    "Олена": "06967652321",
    "Петро": "0123456789"
}
# Запит імені
name = input("Введіть ім'я: ")
# Пошук контакту
if name in phone_book:
    print(f"Номер телефону: {phone_book[name]}")
else:
    print("Контакт не знайдено")
# Словник зручно використовувати для телефонної книги,
