# Створення списку з дублікатами
numbers = [1, 1, 2, 3, 3, 4, 5, 5, 5, 6]
# Перетворення списку на множину
unique_numbers = set(numbers)
# Перетворення множини назад у список
final_list = list(unique_numbers)
# Виведення результату
print(final_list)
# Порядок елементів може змінитися,
# тому що множина (set) не зберігає порядок елементів.
# Її головна особливість — зберігати лише унікальні значення.