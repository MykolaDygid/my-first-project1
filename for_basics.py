# Завдання 1: виведення чисел від 1 до 10
for i in range(1, 11):
    print(i)
# range(1, 11) використовується тому, що кінцеве значення НЕ входить у діапазон.
# Тобто range(1, 11) дає числа від 1 до 10.
print("\nЗворотний відлік:")
# Завдання 2: зворотний відлік від 10 до 1
for i in range(10, 0, -1):
    print(i)
# Третій аргумент у range — це крок.
# -1 означає, що значення буде зменшуватись на 1 на кожній ітерації.