# Створення множин
small = {2, 4, 6}
big = {1, 2, 3, 4, 5, 6, 7}
# Перевірка підмножини
result = small.issubset(big)
# Виведення результату
print(result)
# Метод issubset() перевіряє,
# чи всі елементи однієї множини входять до іншої.
# Оператор in перевіряє лише один конкретний елемент.
#
# Наприклад:
# 2 in big -> True
# А issubset() перевіряє всю множину одразу.