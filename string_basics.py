# Крок : Основи індексації

# Створення змінної
full_name = "Микола Дигідь"

# Індекс — це позиція символу в рядку.
# В Python індексація починається з 0, тобто:
# 0 - перший символ, 1 - другий і т.д.

print("Перший символ:", full_name[0])
print("Третій символ:", full_name[2])
print("Останній символ:", full_name[-1])  # -1 означає останній символ

# Крок : Розділення імені та прізвища

# Введення даних
user_input = input("Введіть ім'я та прізвище: ")

# Пошук пробілу
space_index = user_input.find(" ")

# Витягнення імені та прізвища за допомогою зрізів
first_name = user_input[:space_index]
last_name = user_input[space_index + 1:]

print("Ім'я:", first_name)
print("Прізвище:", last_name)