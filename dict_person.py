# Створення словника з інформацією про людину
person = {
    "name": "Микола",
    "age": 16,
    "city": "Івано-Франківськ",
    "hobby": "Футбол"
}
# Виведення словника
print("Початковий словник:")
print(person)
# Додавання нового поля
person["email"] = "mykola.r.dyhid@ukd.edu.ua"
# Зміна віку
person["age"] = 17
# Видалення хобі
del person["hobby"]
print("\nОновлений словник:")
print(person)
# Виведення ключів
print("\nКлючі словника:")
print(person.keys())
# Виведення значень
print("\nЗначення словника:")
print(person.values())
# Ключ — це назва поля у словнику.
# Значення — це інформація, яка зберігається за цим ключем.
# Наприклад: "name" — ключ, "Микола" — значення.