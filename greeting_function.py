# Функція — це блок коду, який виконує певну задачу.
# Її можна викликати багато разів з різними даними.

def greeting(name, city):
    # Параметри функції — це змінні, які описані в дужках при створенні функції.
    return f"Привіт, {name} з міста {city}!"

# Аргументи функції — це конкретні значення, які передаються під час виклику.

user_name = input("Введіть ваше ім'я: ")
user_city = input("Введіть ваше місто: ")

message = greeting(user_name, user_city)

print(message)

