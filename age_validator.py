# ValueError виникає тоді, коли введені дані
# не можна перетворити у число.
# Число 500 не викликає ValueError, тому що це
# коректне ціле число. Але воно є логічно неправильним віком.
# Помилка типу даних (ValueError) означає, що дані
# мають неправильний формат.
# Логічна помилка означає, що формат правильний,
# але значення не відповідає вимогам програми.
# Поєднання циклу while та try-except дозволяє
# багаторазово запитувати дані, поки користувач
# не введе правильне значення.
while True:
    try:
        age = int(input("Введіть свій вік: "))
    except ValueError:
        print("Помилка: вік потрібно вводити числом.")
        continue
    if age < 1 or age > 120:
        print("Помилка: вік має бути від 1 до 120.")
    else:
        print(f"Вік прийнято: {age}")
        break
print("Дані збережено.")