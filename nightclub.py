# Система доступу до нічного клубу

age = int(input("Введіть ваш вік: "))
passport = input("Чи маєте паспорт? (так/ні): ")
vip = input("Чи маєте VIP статус? (так/ні): ")

# VIP входить завжди
if vip == "так":
    print("Вхід дозволено (VIP)")
else:
    if age >= 18 and passport == "так":
        print("Вхід дозволено")
    elif age < 18:
        print("Відмова: вам менше 18 років")
    else:
        print("Відмова: немає паспорта")